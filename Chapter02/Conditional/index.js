// If else  , switch case and ternary operator are the conditional expression in Js

// If else

let a = 10;

if (a > 5) {
  console.log("good age");
} else {
  console.log("bad age");
}

switch (a) {
  case 1:
    a < 10;
    console.log("Hello");

    break;
  case 2:
    a === 10;
    console.log("World");

    break;
}
